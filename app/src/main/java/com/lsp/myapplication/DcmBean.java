package com.lsp.myapplication;

public class DcmBean {
    long width;
    long height;
    int[] dcm;

    public long getWidth() {
        return width;
    }

    public void setWidth(long width) {
        this.width = width;
    }

    public long getHeight() {
        return height;
    }

    public void setHeight(long height) {
        this.height = height;
    }

    public int[] getDcm() {
        return dcm;
    }

    public void setDcm(int[] dcm) {
        this.dcm = dcm;
    }
}
